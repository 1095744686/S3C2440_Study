/*
	2019.12.04
	点亮第一个LED(GPF4)
=====================================================

	目标：
	1.配置GPFCON寄存器(0x56000050)：设置输出模式
	[9:8] = 01，即0x100写入GPFCON

	-------------------------------------------------
	
	2.配置GPFDAT寄存器(0x56000054)：设置输出低电平
	[4] = 0，即0x10写入GPFDAT
*/

.text
.global _start

_start:
/* GPFCON端口初始化 */
ldr r0, =0x56000050  //伪指令赋值
mov r1, #0x100       //立即数赋值
str r1,[r0]          //将r1的值写入给r0上的地址


/* 设置GPF4低电平 */
ldr r0, =0x56000054
mov r1, #0x0
str r1,[r0]


/* =========================== */
/*   设置死循环,防止错误访问   */

halt:
	b halt           //跳转当前位置











